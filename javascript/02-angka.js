// Tipe data angka non desimal
// Akurat sampai 15 digit
console.log(10); // 10
console.log(123456); // 123456
console.log(999999999999999); // 999999999999999
console.log(9999999999999999); // 10000000000000000

// Tipe data angka desimal
// 3.14, 0.5, 0.3
console.log(3.14); // 3.14
console.log(0.5); // 0.5

// Tipe data angka eksponen
// 123e5, 123e-5
console.log(123e5); // 12300000
console.log(123e-5); // 0.00123

// Tipe data angka bilangan oktal
// 022
console.log(0o22); // 18

// Tipe data angka bilangan hexadecimal
// 0xFF
console.log(0xFF) // 255

// Tipe data angka special
// 2 / 0 infinity
// -2  / 0 -infinity
// 0 / 0 NaN
console.log(2 / 0); // infinity
console.log(-2 / 0); // -infinity
console.log(0 / 0); // NaN (not a number)
console.log(100 / "apple"); // NaN (not a number)
console.log(100 / "10"); // 10